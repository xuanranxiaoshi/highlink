INSERT INTO date_dim
SELECT
    toInt32(formatDateTime(date, '%Y%m%d')) AS date_id,
    date,
    toUInt16(toYear(date)) AS year,
    toUInt8(toMonth(date)) AS month,
    toUInt8(toDayOfMonth(date)) AS day,
    toUInt8((toMonth(date) - 1) / 3 + 1) AS quarter
FROM
    (
        SELECT
            addDays('2024-01-01', number) AS date
        FROM
            (
                SELECT number
                FROM numbers(367)  -- 366 + 1 days, to include 2024-01-01 to 2025-01-01
            )
    )
WHERE date <= '2025-01-01';



